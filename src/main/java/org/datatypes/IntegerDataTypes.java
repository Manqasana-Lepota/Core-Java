package org.datatypes;

public class IntegerDataTypes {
    public static void main(String[] args) {
        int num1 ;

        num1 = 50;
        System.out.println("The value of num1: " + num1);

        num1 = 50/3;
        System.out.println("The value of num1: " + num1);

        byte theByte;
        theByte = -60;
        System.out.println("The value of theByte: " + theByte);

        theByte = (byte) 150;
        System.out.println("The value of theByte: " + theByte);

        short theShort;
        theShort = 233;
        System.out.println("The value of theShort: " + theShort);

        long theLong;
        theLong = 987654321;
        System.out.println("The value of theLong: " + theLong);
    }
}
