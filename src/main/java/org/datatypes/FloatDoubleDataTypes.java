package org.datatypes;

public class FloatDoubleDataTypes {
    public static void main(String[] args) {
        float myFloat;

        myFloat = 50.26F;
        System.out.println("The value of myFloat: " + myFloat);

        myFloat = (float) 50/3;
        System.out.println("The value of myFloat: " + myFloat);

        double myDouble;
        myDouble = -0.2356;
        System.out.println("The value of myDouble: " + myDouble);

        myDouble = 22D/7;
        System.out.println("The value of myDouble: " + myDouble);
    }
}
