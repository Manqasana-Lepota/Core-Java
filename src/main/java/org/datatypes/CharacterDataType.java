package org.datatypes;

public class CharacterDataType {
    public static void main(String[] args) {
        char myChar;

        myChar = 'A';
        System.out.println("The value of myChar: " + myChar);

        myChar = 70;
        System.out.println("The value of myChar: " + myChar);

        myChar = '\u0041';
        System.out.println("The value of myChar: " + myChar);



    }
}
